#Tipo de dado bool (bolean)

'''
Ao questionar algo em um programa,
só existem duas resposta possíveis:
sim (True) ou não (False).

Existem vários operantes para "questionar".

Dentre eles o "==", que é um operador lógico que
questiona se um valor é igual a outro.
'''
print(10 == 10) #sim -> True (verdadeiro)
print(10 == 11) #não -> False (Falso)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))
print("biruleibe" == "biruleibe")
print("biruleibe" == "beibe?")
print(type("biruleibe" == "beibe?"))